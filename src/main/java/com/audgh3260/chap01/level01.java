package com.audgh3260.ex.chap01;

public class level01 {
    public static void main(String[] args) {
        int NUM1 = 20, NUM2 = 30;
        System.out.println("더하기 결과 : " + (NUM1 + NUM2));
        System.out.println("빼기 결과 : " + (NUM1 - NUM2));
        System.out.println("곱하기 결과 : " + (NUM1 * NUM2));
        System.out.println("나누기한 몫 : " + (NUM1 / NUM2));
        System.out.println("나누기한 나머지 : " + (NUM1 % NUM2));

        double width = 12.5d, height = 36.4d;

        System.out.println("면적 : " + (height * width));
        System.out.println("둘레 : " + 2 * (height+ width));
    }

}
